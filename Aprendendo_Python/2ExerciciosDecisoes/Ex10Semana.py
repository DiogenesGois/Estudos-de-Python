# dia da semana


dia_semana = int(input("Indique o numero relativo ao dia da semana\n"))

if dia_semana == 1:
    print("Domingo")
elif dia_semana == 2:
    print("Segunda")
elif dia_semana == 3:
    print("Terca")
elif dia_semana == 4:
    print("Quarta")
elif dia_semana == 5:
    print("Quinta")
elif dia_semana == 6:
    print("Sexta")
elif dia_semana == 7:
    print("Sabado")
else:
    print("Valor invalido")